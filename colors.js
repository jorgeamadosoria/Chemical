const PTABLE_COLOR = "ptable";
const JMOL_COLOR = "jmol";
const GREYSCALE_COLOR = "grey";

function getColor(ele, color) {
  switch (color) {
    case PTABLE_COLOR:
      if (COLORS[ele.symbol]) return COLORS[ele.symbol].ptable;
      return stringToColour(ele, color);
    case JMOL_COLOR:
      if (COLORS[ele.symbol]) return COLORS[ele.symbol].jmol;
      return stringToColour(ele, color);
    case GREYSCALE_COLOR:
      if (COLORS[ele.symbol]) return COLORS[ele.symbol].grey;
      return generateGrey(ele);
  }

  return stringToColour(ele, color);
}

//https://stackoverflow.com/questions/12043187/how-to-check-if-hex-color-is-too-black
function isLight(c) {
  var rgb = parseInt(c, 16); // convert rrggbb to decimal
  var r = (rgb >> 16) & 0xff; // extract red
  var g = (rgb >> 8) & 0xff; // extract green
  var b = (rgb >> 0) & 0xff; // extract blue

  var luma = 0.2126 * r + 0.7152 * g + 0.0722 * b; // per ITU-R BT.709
  return luma < 60 ? "white" : "black";
}

function generateGrey(ele, color) {
  var code = ele.weight;
  var bg = code.toString(16) + code.toString(16);

  bg = ele.weight % 2 ? bg.slice(0, 2) : bg.slice(0, 2)[1] + bg.slice(0, 2)[0];

  bg = bg + bg + bg;
  return {
    stroke: isLight(bg),
    bg: bg,
  };
}

function stringToColour(ele, color) {
  var code = ele.weight;
  var bg = code.toString(16);
  if (code >= 100) bg = code * 1000 + code;
  else if (code >= 10) bg = code * 10000 + code * 100 + code;

  return {
    stroke: isLight(bg),
    bg: bg,
  };
}
